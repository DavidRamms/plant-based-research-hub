def _make_env() -> Environment:
    """Create a Jinja2 environment with our inline templates."""
    env = Environment(loader=BaseLoader())
    env.globals["last_updated"] = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")

    # Register templates
    template_map = {
        "base.html": BASE_HTML,
        "index.html": INDEX_TEMPLATE,
        "topics_index.html": TOPICS_INDEX_TEMPLATE,
        "topic_page.html": TOPIC_PAGE_TEMPLATE,
        "database.html": DATABASE_TEMPLATE,
        "about.html": ABOUT_TEMPLATE,
    }

    # Patch the loader to serve our inline templates
    class InlineLoader(BaseLoader):
        def get_source(self, environment, template):
            if template in template_map:
                source = template_map[template]
                return source, template, lambda: True
            raise Exception(f"Template not found: {template}")

    env.loader = InlineLoader()
    return env


def _determine_strongest_evidence(studies: list[dict]) -> str:
    if not studies:
        return "none"
    tiers = [s.get("quality_tier") for s in studies if s.get("quality_tier")]
    if not tiers:
        return "observational"
    best = min(tiers)
    mapping = {
        1: "meta-analyses / systematic reviews",
        2: "randomized controlled trials",
        3: "cohort / observational studies",
        4: "cross-sectional studies",
        5: "case reports / opinions",
    }
    return mapping.get(best, "observational")


def _has_rct_or_meta(studies: list[dict]) -> bool:
    return any(s.get("quality_tier", 5) <= 2 for s in studies)


def build_static_site(conn: sqlite3.Connection, docs_path: Path) -> None:
    """Generate all static HTML files into docs_path."""
    docs_path = Path(docs_path)
    docs_path.mkdir(parents=True, exist_ok=True)

    env = _make_env()
    last_updated = datetime.utcnow().strftime("%Y-%m-%d %H:%M UTC")

    # Ensure .nojekyll exists
    (docs_path / ".nojekyll").touch()

    all_studies = get_all_studies(conn)
    total_studies = len(all_studies)

    # Collect topic data
    topic_list = []
    for topic_key in sorted(TOPICS.keys()):
        cfg = TOPICS[topic_key]
        count = get_study_count(conn, topic_key)
        summary_row = get_summary(conn, topic_key)
        consensus_text = summary_row["current_consensus"] if summary_row else None
        topic_list.append({
            "key": topic_key,
            "name": cfg["name"],
            "description": cfg["description"],
            "not_fully_plant_based": cfg.get("not_fully_plant_based", False),
            "study_count": count,
            "summary": consensus_text,
        })

    # ------------------------------------------------------------------
    # 1. Homepage: docs/index.html
    # ------------------------------------------------------------------
    tmpl = env.get_template("index.html")
    html = tmpl.render(
        topics=topic_list,
        topic_count=len(TOPICS),
        total_studies=total_studies,
        last_updated=last_updated,
        root_prefix="",
        assets_prefix="",
    )
    (docs_path / "index.html").write_text(html, encoding="utf-8")
    print(f"  Built: docs/index.html")

    # ------------------------------------------------------------------
    # 2. Topics index: docs/topics/index.html
    # ------------------------------------------------------------------
    topics_dir = docs_path / "topics"
    topics_dir.mkdir(exist_ok=True)

    tmpl = env.get_template("topics_index.html")
    html = tmpl.render(
        topics=topic_list,
        topic_count=len(TOPICS),
        last_updated=last_updated,
        root_prefix="../",
        assets_prefix="../",
    )
    (topics_dir / "index.html").write_text(html, encoding="utf-8")
    print(f"  Built: docs/topics/index.html")

    # ------------------------------------------------------------------
    # 3. Individual topic pages: docs/topics/{key}/index.html
    # ------------------------------------------------------------------
    tmpl = env.get_template("topic_page.html")
    for topic_key, cfg in TOPICS.items():
        from database import get_studies_for_topic
        topic_studies = get_studies_for_topic(conn, topic_key, min_quality_tier=5)
        # Sort by pub_year DESC
        topic_studies.sort(key=lambda s: s.get("pub_year") or 0, reverse=True)

        summary_row = get_summary(conn, topic_key)
        strongest = _determine_strongest_evidence(topic_studies)
        show_warning = not _has_rct_or_meta(topic_studies)

        topic_dir = topics_dir / topic_key
        topic_dir.mkdir(exist_ok=True)

        last_upd = summary_row["last_updated"][:10] if summary_row and summary_row.get("last_updated") else None

        html = tmpl.render(
            topic_name=cfg["name"],
            topic_description=cfg["description"],
            not_fully_plant_based=cfg.get("not_fully_plant_based", False),
            study_count=len(topic_studies),
            strongest_evidence=strongest,
            last_updated=last_upd,
            no_rct_warning=show_warning,
            summary=summary_row,
            studies=topic_studies,
            root_prefix="../../",
            assets_prefix="../../",
        )
        (topic_dir / "index.html").write_text(html, encoding="utf-8")
    print(f"  Built: docs/topics/{{topic}}/index.html ({len(TOPICS)} pages)")

    # ------------------------------------------------------------------
    # 4. Database page: docs/database/index.html
    # ------------------------------------------------------------------
    db_dir = docs_path / "database"
    db_dir.mkdir(exist_ok=True)

    tmpl = env.get_template("database.html")
    html = tmpl.render(
        total_studies=total_studies,
        studies=all_studies,
        topics=TOPICS,
        last_updated=last_updated,
        root_prefix="../",
        assets_prefix="../",
    )
    (db_dir / "index.html").write_text(html, encoding="utf-8")
    print(f"  Built: docs/database/index.html")

    # ------------------------------------------------------------------
    # 5. About page: docs/about/index.html
    # ------------------------------------------------------------------
    about_dir = docs_path / "about"
    about_dir.mkdir(exist_ok=True)

    tmpl = env.get_template("about.html")
    html = tmpl.render(
        last_updated=last_updated,
        root_prefix="../",
        assets_prefix="../",
    )
    (about_dir / "index.html").write_text(html, encoding="utf-8")
    print(f"  Built: docs/about/index.html")

    # ------------------------------------------------------------------
    # 6. Search index: docs/search-index.json
    # ------------------------------------------------------------------
    search_index = []
    for study in all_studies:
        abstract = study.get("abstract") or ""
        search_index.append({
            "id": study["pmid"],
            "title": study.get("title", ""),
            "abstract_excerpt": abstract[:200],
            "topics": study.get("topics", []),
            "year": study.get("pub_year"),
            "study_type": study.get("study_type", ""),
        })
    (docs_path / "search-index.json").write_text(
        json.dumps(search_index, ensure_ascii=False, indent=None),
        encoding="utf-8",
    )
    print(f"  Built: docs/search-index.json ({len(search_index)} entries)")

    # ------------------------------------------------------------------
    # 7. Assets (CSS + JS)
    # ------------------------------------------------------------------
    assets_dir = docs_path / "assets"
    assets_dir.mkdir(exist_ok=True)

    print(f"  Site build complete. Total studies: {total_studies}")
